for item in 'James':
    print(item)


for item in ['a', 'b', 'c']:
    print(item)


######### Iterate over a number
for item in [1,2,34,3]:
    print(item)


######### Iterate over a range (comes out with indexing)
for item in range(10):
    print(item)


for items in range(5,9):
    print(items)


for items in range(5,19,2):   # The 2 represent the step i.e jump in 2s
    print(items)